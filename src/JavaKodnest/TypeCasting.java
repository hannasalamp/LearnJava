package JavaKodnest;

public class TypeCasting {
    public static  void main(String[] args){
        //implicit type cating
        int water=123;
        double dubWater=water;
        System.out.println("water is="+water);
        System.out.println("double water is="+dubWater);
        //explicit typecasting
        double salary=3678590;
        int smallSalary=(int)salary;
        System.out.println("smallSalary is="+smallSalary);

    }
}
